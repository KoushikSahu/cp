fn solve<R: BufRead, W: Write>(sc: &mut Scanner<R>, wr: &mut W) {
    let n = sc.next::<usize>();
    let mut strs: Vec<Vec<char>> = vec![];
    let mut longest = 0;
    for i in 0..n {
        let s = sc.next::<String>().chars().collect::<Vec<char>>(); 
        longest = longest.max(s.len());
        strs.push(s);
    }
    for i in 0..n {
        let k = (longest - strs[i].len()) / 2;
        writeln!(wr, "{}{}{}", ".".repeat(k), strs[i].iter().collect::<String>(), ".".repeat(k)).unwrap();
    }
}

